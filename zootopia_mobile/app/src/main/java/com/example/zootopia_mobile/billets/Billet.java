package com.example.zootopia_mobile.billets;

public class Billet {
    private int id_billet;
    private String nom;
    private String description;
    private String prix;

    public int getId_billet() {
        return id_billet;
    }

    public String getNom() {
        return nom;
    }

    public String getDescription() {
        return description;
    }

    public String getPrix() {
        return prix;
    }
}
